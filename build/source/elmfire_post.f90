! *****************************************************************************
SUBROUTINE POPULATE_RASTER_FROM_GRIDFIRE_BINARY_FILE(FOUR, SEVEN)
! *****************************************************************************

CHARACTER(4), INTENT(IN) :: FOUR
CHARACTER(7), INTENT(IN) :: SEVEN
CHARACTER(400) :: FN
INTEGER :: I, ISTART, J

FN = TRIM(OUTPUTS_DIRECTORY) // 'toa_' // FOUR // '_' // SEVEN // '.bin'
