   DO I = 1, NUM_BURNED
      TOA ( BINARY_OUTPUTS_IX_GRIDFIRE(I), BINARY_OUTPUTS_IY_GRIDFIRE(I) ) = BINARY_OUTPUTS_TOA (I)  - FIREMODEL_SIMULATION_TSTART
   ENDDO
ENDIF

